public class uncaught
{
	public static void main(String[] args)
	{
		System.out.println(1/0);
		System.out.println("Hi");
	}
}

/*1/0 exception is run-time excpetion.
We call it uncaught as we're not trying to catch it or handle it. The moment it finds this error program stops executing abnormally. Doesn't execute nything after this. Before this executes everything. */
